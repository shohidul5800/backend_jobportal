     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $user = User::create([
            'user_type' => request('user_type'),
            'email' => request('email'),
            'password' => Hash::make(request('password')),
        ]);
        Company::create([
            'user_id' => $user->id,
            'cname' => request('cname'),
            'email' => request('email'),
            'phone' => request('phone'),
        ]);
        return redirect()->to('login');
    }

